"""
某公司员工的工资计算方法如下：一周内工作时间不超过40小时，按正常工作时间计酬；超出40小时的工作时间部分，按正常工作时间
报酬的1.5倍计酬。员工按进公司时间分为新职工和老职工，进公司不少于5年的员工为老职工，5年以下的为新职工。新职工的
正常工资为30元/小时，老职工的正常工资为50元/小时。请按该计酬方式计算员工的工资。
输入在一行中给出2个正整数，分别为某员工入职年数和周工作时间，其间以空格分隔。
"""
year,hour = map(int,input().split())
if year < 5:
    if hour <= 40:
        gz = hour * 30
    else:
        gz = 1200 + (hour-40)*45
else:
    if hour <= 40:
        gz = hour * 50
    else:
        gz = 2000 +(hour-40)*75
print("%.2f"%gz)