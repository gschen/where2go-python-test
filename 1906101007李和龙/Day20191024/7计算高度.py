
"""
7、	一球从100米高度自由落下，每次落地后反跳回原高度的一半；再落下，求它在第10次落地时，共经过多少米？第10次反弹多高？
"""
a = int(input("请输入求多少次掉地下时："))
b = 100
c = 100
d = 50
if a == 1:
    print("第1次掉地时共经过了%d米，第一次反弹%d米"%(b,b/2))
if a > 1:
    for i in range(a-1):
        c += 2*d
        d = d/2
    print("第",a,"次掉地时共经过了",c,"米，第",a,"次反弹",d,"米")
