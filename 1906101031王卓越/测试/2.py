# 第5只猴子醒来，重新把香蕉均分成5堆，哈哈，正好不剩！

# 请计算一开始最少有多少个香蕉。

# 需要提交的是一个整数，不要填写任何多余的内容。
for a in range(10000):
    s=a
    for i in range(1,5):
        a=a/5-i
        if round(abs(a))!=a:
            break
    if i==4 and a%5==0 and a!=0:
        print(s)
        break
